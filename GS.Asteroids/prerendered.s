        tax

        lda evenOdd
        and #1
        cmp #1
        beq odd

; even
        lda >SCREEN_ADDR,x
        ora #$f0
        sta >SCREEN_ADDR,x
        bra next
odd anop
        lda >SCREEN_ADDR,x
        ora #$0f
        sta >SCREEN_ADDR,x

next anop
        inc counter
        lda counter
        cmp pixelCount
        beq prerenderObjectDone
        jmp loop

prerenderObjectDone anop
        rtl


